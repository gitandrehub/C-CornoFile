#include <stdio.h>

int main(){
    int matrice[10][10] = {{10, 11, 14 ,24, 56, 42, 78, 89, 90, 50},
                           {10, 11, -14 ,24, 56, 42, 78, 89, 90, 50},
                           {10, 11, 14 ,24, 56, 42, -78, 89, 90, 50},
                           {10, -11, 14 ,24, 56, -42, 78, 89, 90, 50},
                           {-10, -11, -14 ,-24, -56, -42, -78, -89, -90, -50},
                           {10, -11, 14 ,24, 56, -42, 78, 89, 90, 50},
                           {10, -11, 14 ,24, 56, -42, 78, 89, 90, 50},
                           {-10, -11, -14 ,-24, -56, -42, -78, -89, -90, -50},
                           {-10, -11, -14 ,-24, -56, -42, -78, -89, -90, -50},
                           {-10, -11, -14 ,-24, -56, -42, -78, -89, -90, -50}};
    int elementi = 10, cont = 0;

    for(int i = 0; i<10; i++){
        for(int j = 0; j <10;j++){
            if(matrice[i][j] < 0){
                elementi -= 1;
            }
        }
        if(elementi == 0){
            cont ++;
        }
        elementi = 10;
    }
    printf("Righe con solo elementi negativi: %d", cont);
    return 0;
}